#!/usr/bin/env node
import { readFileSync } from "node:fs";
import path from "node:path";

const SCHEMA_VERSION = "agent-cli-runtime.publishedVerification.v1";
const REQUIRED_GATES = [
  { script: "smoke:published", command: "npm run smoke:published", schemaVersion: "agent-cli-runtime.publishedSmoke.v1" },
  { script: "published:daemon:verify", command: "npm run published:daemon:verify", schemaVersion: "agent-runtime.publishedDaemonConsumer.v1" },
  { script: "published:adapters:verify", command: "npm run published:adapters:verify", schemaVersion: "agent-runtime.publishedAdapters.v1" },
  { script: "release:post-alpha:verify", command: "npm run release:post-alpha:verify", schemaVersion: "agent-cli-runtime.postAlphaEvidence.v1" },
];

function parseArgs(argv) {
  const options = { dir: "published-verification", summary: undefined, selfTest: false };
  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];
    if (arg === "--dir") options.dir = argv[++i];
    else if (arg === "--summary") options.summary = argv[++i];
    else if (arg === "--self-test") options.selfTest = true;
    else if (arg === "--help" || arg === "-h") {
      process.stdout.write(`Usage: node scripts/verify-published-verification-evidence.mjs [--dir <dir>] [--summary <file>] [--self-test]

Validates published package verification evidence generated by npm run published:verify.
`);
      process.exit(0);
    } else {
      throw new Error(`Unknown argument: ${redact(arg)}`);
    }
  }
  return options;
}

function redact(value) {
  if (typeof value !== "string") return value;
  return value
    .replace(/sk-?[A-Za-z0-9_-]{20,}/gu, "[REDACTED]")
    .replace(/\bBearer\s+(?!<)[A-Za-z0-9+/_=.:-]{10,}\b/gu, "Bearer [REDACTED]")
    .replace(/\b[A-Z_]*(?:TOKEN|API_KEY)[A-Z_]*\s*=\s*(?!<|\$|\$\{|\[REDACTED\]|redacted\b)[^\s#'"]{4,}/giu, "[REDACTED_ENV]=[REDACTED]")
    .replace(/\/private\/tmp\/[^\s"']+/gu, "<path>")
    .replace(/\/tmp\/[^\s"']+/gu, "<path>")
    .replace(/\/var\/folders\/[^\s"']+/gu, "<path>")
    .replace(/\/Users\/[^/\s"']+(?:\/[^\s"']*)?/gu, "/" + "Users/[REDACTED]")
    .replace(/\/home\/[^/\s"']+(?:\/[^\s"']*)?/gu, "/" + "home/[REDACTED]")
    .replace(/[A-Z]:\\Users\\[^\\\s"']+(?:\\[^\s"']*)?/gu, "C:" + "\\Users\\[REDACTED]");
}

function containsUnsafe(value) {
  const text = JSON.stringify(value);
  return [
    process.cwd(),
    process.env.HOME,
    "/tmp/",
    "/private/tmp/",
    "/var/folders/",
    "Bearer " + "B".repeat(20),
    "sk-" + "A".repeat(24),
    "P5_PUBLISHED_ADAPTER_COMPAT_PROMPT_",
  ].filter(Boolean).some((needle) => text.includes(needle)) ||
    /\b[A-Z_]*(?:TOKEN|API_KEY)[A-Z_]*\s*=\s*(?!<|\$|\$\{|\[REDACTED\]|redacted\b)[^\s#'"]{4,}/iu.test(text) ||
    /\/Users\/[^/\s"']+|\/home\/[^/\s"']+|[A-Z]:\\Users\\/u.test(text);
}

function addDiagnostic(diagnostics, code, message, details = {}) {
  diagnostics.push({
    code,
    message: redact(message),
    ...Object.fromEntries(Object.entries(details).map(([key, value]) => [key, typeof value === "string" ? redact(value) : value])),
  });
}

function validate(summary) {
  const diagnostics = [];
  if (summary?.schemaVersion !== SCHEMA_VERSION) {
    addDiagnostic(diagnostics, "invalid_schema", "Published verification schema version is missing or unsupported.", {
      expected: SCHEMA_VERSION,
      actual: typeof summary?.schemaVersion === "string" ? summary.schemaVersion : null,
    });
  }
  for (const field of ["ok", "packageName", "version", "gitSha", "checkedAt", "packageSource", "gates", "registry", "diagnostics"]) {
    if (!(field in (summary ?? {}))) addDiagnostic(diagnostics, "missing_field", `Published verification summary is missing ${field}.`, { field });
  }
  if (summary?.packageSource !== "npm-registry") addDiagnostic(diagnostics, "invalid_package_source", "packageSource must be npm-registry.");
  if (summary?.noAuthenticatedRealRun !== true) addDiagnostic(diagnostics, "unsafe_boundary", "noAuthenticatedRealRun must be true.");
  if (summary?.noNpmPublish !== true) addDiagnostic(diagnostics, "unsafe_boundary", "noNpmPublish must be true.");
  if (summary?.noNpmToken !== true) addDiagnostic(diagnostics, "unsafe_boundary", "noNpmToken must be true.");

  const gates = Array.isArray(summary?.gates) ? summary.gates : [];
  if (!Array.isArray(summary?.gates)) addDiagnostic(diagnostics, "invalid_gates", "gates must be an array.");
  for (const required of REQUIRED_GATES) {
    const gate = gates.find((item) => item.script === required.script);
    if (!gate) {
      addDiagnostic(diagnostics, "missing_gate", `Missing required gate ${required.script}.`, { script: required.script });
      continue;
    }
    if (gate.command !== required.command) addDiagnostic(diagnostics, "invalid_gate_command", `Gate ${required.script} command is not stable.`, { script: required.script });
    if (gate.schemaVersion !== required.schemaVersion) addDiagnostic(diagnostics, "invalid_gate_schema", `Gate ${required.script} schema version is unsupported.`, { script: required.script });
    if (gate.ok !== true) addDiagnostic(diagnostics, "failed_gate", `Gate ${required.script} did not pass.`, { script: required.script });
    if (typeof gate.durationMs !== "number") addDiagnostic(diagnostics, "invalid_gate_duration", `Gate ${required.script} is missing durationMs.`, { script: required.script });
  }

  if (summary?.registry?.ok !== true) addDiagnostic(diagnostics, "registry_lookup_failed", "npm registry metadata lookup did not pass.");
  if (summary?.registry?.summary?.version !== summary?.version) {
    addDiagnostic(diagnostics, "registry_version_mismatch", "npm registry metadata version does not match package.json version.");
  }
  if (containsUnsafe(summary)) addDiagnostic(diagnostics, "unsafe_content", "Published verification evidence contains unredacted sensitive content.");
  if (summary?.ok !== (diagnostics.length === 0)) {
    addDiagnostic(diagnostics, "ok_mismatch", "Top-level ok does not match verifier diagnostics.");
  }
  return {
    schemaVersion: SCHEMA_VERSION,
    ok: diagnostics.length === 0,
    packageName: summary?.packageName ?? null,
    version: summary?.version ?? null,
    checkedGates: gates.map((gate) => gate.script).filter(Boolean),
    diagnostics,
    noAuthenticatedRealRun: summary?.noAuthenticatedRealRun === true,
    noNpmPublish: summary?.noNpmPublish === true,
    noNpmToken: summary?.noNpmToken === true,
  };
}

function fixture({ ok = true, unsafe = false } = {}) {
  return {
    schemaVersion: SCHEMA_VERSION,
    ok,
    packageName: "agent-cli-runtime",
    version: "0.1.0-alpha.1",
    gitSha: "0123456789abcdef0123456789abcdef01234567",
    checkedAt: "2026-06-23T00:00:00.000Z",
    packageSource: "npm-registry",
    gates: REQUIRED_GATES.map((gate) => ({
      name: gate.script,
      script: gate.script,
      command: gate.command,
      ok: gate.script === "published:daemon:verify" ? ok : true,
      schemaVersion: gate.schemaVersion,
      durationMs: 1,
      packageSource: gate.script === "release:post-alpha:verify" ? null : "npm-registry",
      summary: { packageSource: "npm-registry" },
      diagnostics: gate.script === "published:daemon:verify" && !ok
        ? [{ code: "fixture_failure", message: unsafe ? `leaked sk-${"A".repeat(24)} Bearer ${"B".repeat(20)} /tmp/private` : "fixture failed" }]
        : [],
    })),
    registry: {
      command: "npm view agent-cli-runtime@0.1.0-alpha.1 version dist-tags dist --json",
      ok: true,
      durationMs: 1,
      summary: { version: "0.1.0-alpha.1", distTags: { alpha: "0.1.0-alpha.1" }, dist: { tarball: "agent-cli-runtime-0.1.0-alpha.1.tgz" } },
      diagnostics: [],
    },
    diagnostics: ok ? [] : [{ code: "gate_failed", message: "npm run published:daemon:verify did not pass", gate: "published-daemon" }],
    noAuthenticatedRealRun: true,
    noNpmPublish: true,
    noNpmToken: true,
  };
}

function runSelfTest() {
  const passing = validate(fixture());
  const failing = validate(fixture({ ok: false }));
  const unsafe = validate(fixture({ ok: false, unsafe: true }));
  return {
    schemaVersion: SCHEMA_VERSION,
    ok: passing.ok === true && failing.ok === false && unsafe.ok === false,
    checks: {
      validSummaryAccepted: passing.ok === true,
      failedGateRejected: failing.diagnostics.some((diagnostic) => diagnostic.code === "failed_gate"),
      unsafeContentRejected: unsafe.diagnostics.some((diagnostic) => diagnostic.code === "unsafe_content"),
      safetyFlagsChecked: passing.noAuthenticatedRealRun && passing.noNpmPublish && passing.noNpmToken,
    },
    diagnostics: [],
    noAuthenticatedRealRun: true,
    noNpmPublish: true,
    noNpmToken: true,
  };
}

function main() {
  const options = parseArgs(process.argv.slice(2));
  if (options.selfTest) {
    const result = runSelfTest();
    process.stdout.write(`${JSON.stringify(result, null, 2)}\n`);
    if (!result.ok) process.exit(1);
    return;
  }
  const summaryPath = path.resolve(options.summary ?? path.join(options.dir, "published-verification.json"));
  const summary = JSON.parse(readFileSync(summaryPath, "utf8"));
  const result = validate(summary);
  process.stdout.write(`${JSON.stringify(result, null, 2)}\n`);
  if (!result.ok) process.exit(1);
}

try {
  main();
} catch (error) {
  process.stdout.write(`${JSON.stringify({
    schemaVersion: SCHEMA_VERSION,
    ok: false,
    diagnostics: [{ code: "published_verification_evidence_error", message: redact(error instanceof Error ? error.message : String(error)) }],
    noAuthenticatedRealRun: true,
    noNpmPublish: true,
    noNpmToken: true,
  }, null, 2)}\n`);
  process.exit(1);
}
