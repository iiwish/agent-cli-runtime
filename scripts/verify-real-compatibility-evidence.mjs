#!/usr/bin/env node
import { readFileSync } from "node:fs";
import path from "node:path";

const EVIDENCE_SCHEMA_VERSION = "agent-cli-runtime.realCompatibilityEvidence.v1";
const VERIFICATION_SCHEMA_VERSION = "agent-cli-runtime.realCompatibilityEvidenceVerification.v1";
const DEFAULT_FILE = ".release-evidence/p6-1-real-cli-compatibility.json";
const COMMAND_ALLOWED_KEYS = new Set(["command", "exitCode", "schemaVersion", "ok", "summary"]);
const AUTHENTICATED_SUCCESS_REQUIRED = {
  expectedTextRequired: true,
  expectedTextMatched: true,
  cwdMutationChecked: true,
  cwdMutated: false,
};
const REQUIRED_NEEDS_VERIFICATION = {
  codex: ["session", "authProbe"],
  claude: ["session.id", "reasoning"],
  opencode: ["extraAllowedDirs", "session", "permissionPolicy.read-only"],
};
const UNSAFE_PATTERNS = [
  { code: "unsafe_content", name: "private user path", pattern: /(?:\/Users\/|\/home\/(?:<user>|[^<\s/]+)|[A-Z]:\\Users\\)/u },
  { code: "unsafe_content", name: "OpenAI-style secret", pattern: /sk-[A-Za-z0-9_-]{20,}/u },
  { code: "unsafe_content", name: "Bearer value", pattern: /\bBearer\s+(?!<)[A-Za-z0-9+/_=-]{10,}\b/u },
  {
    code: "unsafe_content",
    name: "auth environment assignment value",
    pattern:
      /\b(?:ANTHROPIC_AUTH_TOKEN|ANTHROPIC_API_KEY|OPENAI_API_KEY|OPENAI_AUTH_TOKEN|CLAUDE_AUTH_TOKEN|CODEX_AUTH_TOKEN|OPENCODE_AUTH_TOKEN)\s*=\s*(?!<|\$|\$\{|\[REDACTED\]|redacted\b)[^\s#'"]{4,}/iu,
  },
];
const AUTHENTICATED_SUCCESS_UNSAFE_KEYS = new Set([
  "stdout",
  "stderr",
  "rawStdout",
  "rawStderr",
  "rawOutput",
  "observedTextTail",
  "promptText",
  "fullPrompt",
]);
const COMMAND_SUMMARY_UNSAFE_KEYS = new Set([
  ...AUTHENTICATED_SUCCESS_UNSAFE_KEYS,
  "finalRunRecord",
]);

function parseArgs(argv) {
  const options = { file: DEFAULT_FILE, selfTest: false };
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--file") {
      options.file = requireValue(argv, ++index, arg);
    } else if (arg === "--self-test") {
      options.selfTest = true;
    } else if (arg === "--help" || arg === "-h") {
      process.stdout.write(`Usage: node scripts/verify-real-compatibility-evidence.mjs [--file <path>] [--self-test]

Validates repo-only real CLI compatibility evidence generated by npm run compat:real:evidence.
`);
      process.exit(0);
    } else {
      throw new Error(`Unknown argument: ${redact(arg)}`);
    }
  }
  return options;
}

function requireValue(argv, index, flag) {
  const value = argv[index];
  if (!value || value.startsWith("--")) throw new Error(`${flag} requires a value`);
  return value;
}

function redact(value) {
  if (typeof value !== "string") return value;
  return value
    .replace(/sk-[A-Za-z0-9_-]{20,}/gu, "[REDACTED]")
    .replace(/\bBearer\s+(?!<)[A-Za-z0-9+/_=-]{10,}\b/gu, "Bearer [REDACTED]")
    .replace(
      /\b(?:ANTHROPIC_AUTH_TOKEN|ANTHROPIC_API_KEY|OPENAI_API_KEY|OPENAI_AUTH_TOKEN|CLAUDE_AUTH_TOKEN|CODEX_AUTH_TOKEN|OPENCODE_AUTH_TOKEN)\s*=\s*(?!<|\$|\$\{|\[REDACTED\]|redacted\b)[^\s#'"]{4,}/giu,
      "[REDACTED_ENV]=[REDACTED]",
    )
    .replace(/\/Users\/[^/\s"']+(?:\/[^\s"']*)?/gu, "/" + "Users/[REDACTED]")
    .replace(/\/home\/(?:<user>|[^<\s/]+)(?:\/[^\s"']*)?/gu, "/" + "home/[REDACTED]")
    .replace(/[A-Z]:\\Users\\[^\\\s"']+(?:\\[^\s"']*)?/gu, "C:" + "\\Users\\[REDACTED]");
}

function addDiagnostic(diagnostics, code, message, details = {}) {
  diagnostics.push({
    code,
    message: redact(message),
    ...Object.fromEntries(
      Object.entries(details).map(([key, value]) => [key, typeof value === "string" ? redact(value) : value]),
    ),
  });
}

function validateEvidence(evidence, rawText = JSON.stringify(evidence)) {
  const diagnostics = [];

  if (evidence?.schemaVersion !== EVIDENCE_SCHEMA_VERSION) {
    addDiagnostic(diagnostics, "invalid_schema", "Real compatibility evidence schema version is missing or unsupported.", {
      expected: EVIDENCE_SCHEMA_VERSION,
      actual: typeof evidence?.schemaVersion === "string" ? evidence.schemaVersion : null,
    });
  }

  checkUnsafeText(rawText, diagnostics);
  checkDirtyState(evidence, diagnostics);
  checkCommands(evidence, diagnostics);
  checkSafeSmokes(evidence, diagnostics);
  checkAuthenticatedSmokes(evidence, diagnostics);
  checkNeedsVerification(evidence, diagnostics);
  checkPackageBoundary(evidence, diagnostics);

  return verificationResult(evidence, diagnostics);
}

function verificationResult(evidence, diagnostics) {
  return {
    schemaVersion: VERIFICATION_SCHEMA_VERSION,
    ok: diagnostics.length === 0,
    evidenceSchemaVersion: evidence?.schemaVersion ?? null,
    file: evidence?.schemaVersion ? DEFAULT_FILE : null,
    checkedAt: new Date().toISOString(),
    diagnostics,
  };
}

function checkUnsafeText(rawText, diagnostics) {
  for (const { code, name, pattern } of UNSAFE_PATTERNS) {
    if (pattern.test(rawText)) addDiagnostic(diagnostics, code, `Real compatibility evidence contains unredacted ${name}.`);
  }
}

function checkDirtyState(evidence, diagnostics) {
  const before = evidence?.gitStatusBeforeWrite;
  const after = evidence?.gitStatusAfterWrite;
  for (const field of ["gitHeadSha", "gitDirty", "gitStatusBeforeWrite", "gitStatusAfterWrite"]) {
    if (!(field in (evidence ?? {}))) {
      addDiagnostic(diagnostics, "missing_dirty_state", `Evidence is missing ${field}.`, { field });
    }
  }
  if (typeof evidence?.gitHeadSha !== "string" || !/^[0-9a-f]{40}$/u.test(evidence.gitHeadSha)) {
    addDiagnostic(diagnostics, "missing_dirty_state", "gitHeadSha must be a full commit SHA.");
  }
  if (typeof evidence?.gitDirty !== "boolean") {
    addDiagnostic(diagnostics, "missing_dirty_state", "gitDirty must be a boolean.");
  }
  for (const [label, state] of [["gitStatusBeforeWrite", before], ["gitStatusAfterWrite", after]]) {
    if (typeof state?.dirty !== "boolean" || typeof state?.changedFilesCount !== "number" || !Array.isArray(state?.changedFiles)) {
      addDiagnostic(diagnostics, "missing_dirty_state", `${label} must include dirty, changedFilesCount, and changedFiles.`, {
        field: label,
      });
    }
    if (state?.headSha && state.headSha !== evidence?.gitHeadSha) {
      addDiagnostic(diagnostics, "missing_dirty_state", `${label} headSha does not match gitHeadSha.`, { field: label });
    }
  }
  if (typeof after?.dirty === "boolean" && evidence?.gitDirty !== after.dirty) {
    addDiagnostic(diagnostics, "missing_dirty_state", "gitDirty must match gitStatusAfterWrite.dirty.");
  }
  if (evidence?.gitDirty === true && /clean\s+commit\s+evidence|cleanCommitEvidence/iu.test(JSON.stringify(evidence))) {
    addDiagnostic(diagnostics, "missing_dirty_state", "Dirty-tree evidence must not be described as clean commit evidence.");
  }
}

function checkCommands(evidence, diagnostics) {
  const commands = evidence?.commands;
  if (!Array.isArray(commands) || commands.length === 0) {
    addDiagnostic(diagnostics, "invalid_schema", "commands must be a non-empty array.");
    return;
  }
  commands.forEach((command, index) => {
    const extraKeys = Object.keys(command ?? {}).filter((key) => !COMMAND_ALLOWED_KEYS.has(key));
    if (extraKeys.length > 0) {
      addDiagnostic(diagnostics, "unsafe_content", "Command summary contains fields outside the redacted allowlist.", { index });
    }
    if (typeof command?.command !== "string" || typeof command?.exitCode !== "number") {
      addDiagnostic(diagnostics, "invalid_schema", "Command summary must include command and exitCode.", { index });
    }
    if ("stdout" in (command ?? {}) || "stderr" in (command ?? {}) || "rawStdout" in (command ?? {}) || "rawStderr" in (command ?? {})) {
      addDiagnostic(diagnostics, "unsafe_content", "Command summary must not include raw stdout or stderr.", { index });
    }
    if (containsUnsafeCommandSummaryField(command?.summary)) {
      addDiagnostic(diagnostics, "unsafe_content", "Command summary must not include raw output, prompt text, or final run records.", {
        index,
      });
    }
  });
}

function containsUnsafeCommandSummaryField(value) {
  const stack = [value];
  while (stack.length > 0) {
    const current = stack.pop();
    if (!current || typeof current !== "object") continue;
    for (const [key, child] of Object.entries(current)) {
      if (COMMAND_SUMMARY_UNSAFE_KEYS.has(key)) return true;
      if (key === "prompt" && typeof child === "string") return true;
      if (child && typeof child === "object") stack.push(child);
    }
  }
  return false;
}

function checkSafeSmokes(evidence, diagnostics) {
  const safeSmokes = evidence?.safeRealSmokes;
  if (!safeSmokes || typeof safeSmokes !== "object") {
    addDiagnostic(diagnostics, "invalid_schema", "safeRealSmokes must be present.");
    return;
  }
  const expectedDefaults = {
    codex: "real_run_skipped",
    claude: "auth_missing",
    opencode: "real_run_skipped",
  };
  for (const [agent, expected] of Object.entries(expectedDefaults)) {
    const smoke = safeSmokes[agent];
    if (!smoke) {
      addDiagnostic(diagnostics, "invalid_schema", `safeRealSmokes is missing ${agent}.`, { agent });
      continue;
    }
    if (smoke.runClassification === "success" || smoke.ok === true) {
      addDiagnostic(diagnostics, "skip_state_claimed_as_success", `Safe real smoke for ${agent} must not be counted as success.`, {
        agent,
      });
    }
    if (smoke.runClassification !== expected) {
      addDiagnostic(diagnostics, "skip_state_claimed_as_success", `Safe real smoke for ${agent} has unexpected default classification.`, {
        agent,
        expected,
        actual: smoke.runClassification ?? null,
      });
    }
  }
}

function checkAuthenticatedSmokes(evidence, diagnostics) {
  const smokes = evidence?.authenticatedRealSmokes;
  if (!Array.isArray(smokes)) {
    addDiagnostic(diagnostics, "invalid_schema", "authenticatedRealSmokes must be an array.");
    return;
  }
  for (const [index, smoke] of smokes.entries()) {
    if (smoke?.ok === true && smoke.runClassification !== "success") {
      addDiagnostic(diagnostics, "skip_state_claimed_as_success", "Authenticated smoke ok=true must only be used with success.", { index });
    }
    if (containsUnsafeAuthenticatedField(smoke)) {
      addDiagnostic(diagnostics, "unsafe_content", "Authenticated smoke contains raw output, prompt text, token, or private path fields.", {
        index,
      });
    }
    if (smoke?.runClassification !== "success") continue;
    for (const [field, expected] of Object.entries(AUTHENTICATED_SUCCESS_REQUIRED)) {
      if (smoke[field] !== expected) {
        addDiagnostic(diagnostics, "authenticated_success_incomplete", `Authenticated success is missing required ${field} evidence.`, {
          index,
          field,
        });
      }
    }
  }
}

function containsUnsafeAuthenticatedField(value) {
  const stack = [value];
  while (stack.length > 0) {
    const current = stack.pop();
    if (!current || typeof current !== "object") continue;
    for (const [key, child] of Object.entries(current)) {
      if (AUTHENTICATED_SUCCESS_UNSAFE_KEYS.has(key)) return true;
      if (key === "prompt" && typeof child === "string") return true;
      if (child && typeof child === "object") stack.push(child);
    }
  }
  return false;
}

function checkNeedsVerification(evidence, diagnostics) {
  const audit = Array.isArray(evidence?.needsVerificationAudit) ? evidence.needsVerificationAudit : [];
  if (audit.length === 0) {
    addDiagnostic(diagnostics, "needs_verification_missing", "needsVerificationAudit must be present.");
    return;
  }
  for (const [adapter, requiredItems] of Object.entries(REQUIRED_NEEDS_VERIFICATION)) {
    const item = audit.find((candidate) => candidate?.adapter === adapter);
    const mapsTo = new Set((item?.items ?? []).map((candidate) => candidate?.mapsTo).filter(Boolean));
    for (const required of requiredItems) {
      if (!mapsTo.has(required)) {
        addDiagnostic(diagnostics, "needs_verification_missing", `Required needsVerification item is missing for ${adapter}.`, {
          adapter,
          item: required,
        });
      }
    }
  }
}

function checkPackageBoundary(evidence, diagnostics) {
  if (evidence?.packageBoundary?.releaseEvidenceIsRepoOnly !== true) {
    addDiagnostic(diagnostics, "package_boundary_invalid", "packageBoundary.releaseEvidenceIsRepoOnly must be true.");
  }
}

function validFixture() {
  const state = {
    headSha: "0123456789abcdef0123456789abcdef01234567",
    dirty: true,
    changedFilesCount: 1,
    changedFiles: [{ status: "M", path: "scripts/create-real-compatibility-evidence.mjs" }],
    truncated: false,
  };
  return {
    schemaVersion: EVIDENCE_SCHEMA_VERSION,
    generatedAt: "2026-06-24T00:00:00.000Z",
    gitHeadSha: state.headSha,
    gitShaMeaning: "HEAD commit only; see gitDirty for uncommitted evidence inputs.",
    gitDirty: true,
    gitStatusBeforeWrite: state,
    gitStatusAfterWrite: state,
    commands: [
      {
        command: "node ./dist/cli/main.js smoke --mode real --agent codex --json",
        exitCode: 0,
        schemaVersion: "agent-runtime.realSmoke.v1",
        ok: false,
        summary: { runClassification: "real_run_skipped" },
      },
    ],
    safeRealSmokes: {
      codex: { ok: false, runClassification: "real_run_skipped" },
      claude: { ok: false, runClassification: "auth_missing" },
      opencode: { ok: false, runClassification: "real_run_skipped" },
    },
    authenticatedRealSmokes: [
      {
        agent: "codex",
        runClassification: "success",
        ok: true,
        expectedTextRequired: true,
        expectedTextMatched: true,
        cwdMutationChecked: true,
        cwdMutated: false,
        expectedTextSha256: "0".repeat(64),
        observedTextTailSha256: "1".repeat(64),
      },
      {
        agent: "claude",
        runClassification: "auth_missing",
        ok: false,
      },
    ],
    needsVerificationAudit: [
      { adapter: "codex", items: [{ mapsTo: "session" }, { mapsTo: "authProbe" }] },
      { adapter: "claude", items: [{ mapsTo: "session.id" }, { mapsTo: "reasoning" }] },
      {
        adapter: "opencode",
        items: [{ mapsTo: "extraAllowedDirs" }, { mapsTo: "session" }, { mapsTo: "permissionPolicy.read-only" }],
      },
    ],
    packageBoundary: { releaseEvidenceIsRepoOnly: true },
  };
}

function clone(value) {
  return JSON.parse(JSON.stringify(value));
}

function runSelfTest() {
  const valid = validFixture();
  const unsafe = clone(valid);
  unsafe.commands[0].summary.message = "leaked " + "sk-" + "A".repeat(24);
  const rawCommandOutput = clone(valid);
  rawCommandOutput.commands[0].summary.rawStdout = "raw cli output without secrets";
  const missingDirty = clone(valid);
  delete missingDirty.gitStatusAfterWrite;
  const skipAsSuccess = clone(valid);
  skipAsSuccess.safeRealSmokes.codex = { ok: true, runClassification: "success" };
  const incompleteAuth = clone(valid);
  incompleteAuth.authenticatedRealSmokes[0].expectedTextMatched = false;
  const nonSuccessAuthenticatedRawOutput = clone(valid);
  nonSuccessAuthenticatedRawOutput.authenticatedRealSmokes[1].rawStdout = "raw cli output should be rejected";
  const missingNeedsVerification = clone(valid);
  missingNeedsVerification.needsVerificationAudit[0].items = [{ mapsTo: "session" }];
  const invalidBoundary = clone(valid);
  invalidBoundary.packageBoundary.releaseEvidenceIsRepoOnly = false;

  const validResult = validateEvidence(valid);
  const unsafeResult = validateEvidence(unsafe);
  const rawCommandOutputResult = validateEvidence(rawCommandOutput);
  const missingDirtyResult = validateEvidence(missingDirty);
  const skipAsSuccessResult = validateEvidence(skipAsSuccess);
  const incompleteAuthResult = validateEvidence(incompleteAuth);
  const nonSuccessAuthenticatedRawOutputResult = validateEvidence(nonSuccessAuthenticatedRawOutput);
  const missingNeedsVerificationResult = validateEvidence(missingNeedsVerification);
  const invalidBoundaryResult = validateEvidence(invalidBoundary);
  const checks = {
    validFixtureAccepted: validResult.ok,
    unsafeContentRejected: hasDiagnostic(unsafeResult, "unsafe_content"),
    rawCommandOutputRejected: hasDiagnostic(rawCommandOutputResult, "unsafe_content"),
    missingDirtyStateRejected: hasDiagnostic(missingDirtyResult, "missing_dirty_state"),
    skipStateClaimedAsSuccessRejected: hasDiagnostic(skipAsSuccessResult, "skip_state_claimed_as_success"),
    authenticatedSuccessIncompleteRejected: hasDiagnostic(incompleteAuthResult, "authenticated_success_incomplete"),
    nonSuccessAuthenticatedRawOutputRejected: hasDiagnostic(nonSuccessAuthenticatedRawOutputResult, "unsafe_content"),
    needsVerificationMissingRejected: hasDiagnostic(missingNeedsVerificationResult, "needs_verification_missing"),
    packageBoundaryInvalidRejected: hasDiagnostic(invalidBoundaryResult, "package_boundary_invalid"),
  };

  return {
    schemaVersion: VERIFICATION_SCHEMA_VERSION,
    ok: Object.values(checks).every(Boolean),
    checks,
    diagnostics: [],
  };
}

function hasDiagnostic(result, code) {
  return result.diagnostics.some((diagnostic) => diagnostic.code === code);
}

function main() {
  const options = parseArgs(process.argv.slice(2));
  if (options.selfTest) {
    const result = runSelfTest();
    process.stdout.write(`${JSON.stringify(result, null, 2)}\n`);
    if (!result.ok) process.exit(1);
    return;
  }

  const file = path.resolve(options.file);
  const rawText = readFileSync(file, "utf8");
  const evidence = JSON.parse(rawText);
  const result = validateEvidence(evidence, rawText);
  result.file = path.relative(process.cwd(), file).split(path.sep).join("/");
  process.stdout.write(`${JSON.stringify(result, null, 2)}\n`);
  if (!result.ok) process.exit(1);
}

try {
  main();
} catch (error) {
  process.stdout.write(`${JSON.stringify({
    schemaVersion: VERIFICATION_SCHEMA_VERSION,
    ok: false,
    diagnostics: [
      {
        code: "invalid_schema",
        message: redact(error instanceof Error ? error.message : String(error)),
      },
    ],
  }, null, 2)}\n`);
  process.exit(1);
}
