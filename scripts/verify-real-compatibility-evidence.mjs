#!/usr/bin/env node
import { readFileSync } from "node:fs";
import path from "node:path";

const LEGACY_EVIDENCE_SCHEMA_VERSION = "agent-cli-runtime.realCompatibilityEvidence.v1";
const MATRIX_SCHEMA_VERSION = "agent-cli-runtime.realCompatibilityMatrix.v1";
const VERIFICATION_SCHEMA_VERSION = "agent-cli-runtime.realCompatibilityEvidenceVerification.v1";
const DEFAULT_FILE = ".release-evidence/p8-2-real-cli-compatibility-matrix.json";
const ADAPTERS = ["codex", "claude", "opencode"];
const COMMAND_ALLOWED_KEYS = new Set(["command", "exitCode", "schemaVersion", "ok", "summary"]);
const AUTHENTICATED_SUCCESS_REQUIRED = {
  expectedTextRequired: true,
  expectedTextMatched: true,
  cwdMutationChecked: true,
  cwdMutated: false,
};
const REQUIRED_NEEDS_VERIFICATION = {
  codex: ["session", "authProbe"],
  claude: ["session.id", "reasoning"],
  opencode: ["extraAllowedDirs", "session", "permissionPolicy.read-only"],
};
const UNSAFE_PATTERNS = [
  { code: "unsafe_content", name: "temporary path", pattern: /(?:\/tmp\/|\/private\/tmp\/|\/var\/folders\/)/u },
  { code: "unsafe_content", name: "private user path", pattern: /(?:\/Users\/|\/home\/(?:<user>|[^<\s/]+)|[A-Z]:\\Users\\)/u },
  { code: "unsafe_content", name: "OpenAI-style secret", pattern: /sk-[A-Za-z0-9_-]{20,}/u },
  { code: "unsafe_content", name: "Bearer value", pattern: /\bBearer\s+(?!<)[A-Za-z0-9+/_=-]{10,}\b/u },
  {
    code: "unsafe_content",
    name: "auth environment assignment value",
    pattern:
      /\b(?:ANTHROPIC_AUTH_TOKEN|ANTHROPIC_API_KEY|OPENAI_API_KEY|OPENAI_AUTH_TOKEN|CLAUDE_AUTH_TOKEN|CODEX_AUTH_TOKEN|OPENCODE_AUTH_TOKEN)\s*=\s*(?!<|\$|\$\{|\[REDACTED\]|redacted\b)[^\s#'"]{4,}/iu,
  },
];
const AUTHENTICATED_SUCCESS_UNSAFE_KEYS = new Set([
  "stdout",
  "stderr",
  "rawStdout",
  "rawStderr",
  "rawOutput",
  "observedTextTail",
  "promptText",
  "fullPrompt",
]);
const COMMAND_SUMMARY_UNSAFE_KEYS = new Set([
  ...AUTHENTICATED_SUCCESS_UNSAFE_KEYS,
  "finalRunRecord",
]);

function parseArgs(argv) {
  const options = {
    file: DEFAULT_FILE,
    selfTest: false,
    targetSha: null,
    maxAgeHours: null,
    allowDirty: false,
    releaseStrict: false,
  };
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--file") {
      options.file = requireValue(argv, ++index, arg);
    } else if (arg === "--target-sha") {
      options.targetSha = requireValue(argv, ++index, arg).toLowerCase();
    } else if (arg === "--max-age-hours") {
      options.maxAgeHours = Number(requireValue(argv, ++index, arg));
      if (!Number.isFinite(options.maxAgeHours) || options.maxAgeHours <= 0) {
        throw new Error("--max-age-hours must be a positive number");
      }
    } else if (arg === "--allow-dirty") {
      options.allowDirty = true;
    } else if (arg === "--release-strict") {
      options.releaseStrict = true;
    } else if (arg === "--self-test") {
      options.selfTest = true;
    } else if (arg === "--help" || arg === "-h") {
      process.stdout.write(`Usage: node scripts/verify-real-compatibility-evidence.mjs [--file <path>] [--target-sha <sha>] [--max-age-hours <n>] [--release-strict] [--allow-dirty] [--self-test]

Validates repo-only real CLI compatibility evidence generated by npm run compat:real:evidence.

Release review mode should pass --target-sha, --max-age-hours, and --release-strict. Dirty release evidence requires explicit --allow-dirty.
`);
      process.exit(0);
    } else {
      throw new Error(`Unknown argument: ${redact(arg)}`);
    }
  }
  return options;
}

function requireValue(argv, index, flag) {
  const value = argv[index];
  if (!value || value.startsWith("--")) throw new Error(`${flag} requires a value`);
  return value;
}

function redact(value) {
  if (typeof value !== "string") return value;
  return value
    .replace(/sk-[A-Za-z0-9_-]{20,}/gu, "[REDACTED]")
    .replace(/\bBearer\s+(?!<)[A-Za-z0-9+/_=-]{10,}\b/gu, "Bearer [REDACTED]")
    .replace(
      /\b(?:ANTHROPIC_AUTH_TOKEN|ANTHROPIC_API_KEY|OPENAI_API_KEY|OPENAI_AUTH_TOKEN|CLAUDE_AUTH_TOKEN|CODEX_AUTH_TOKEN|OPENCODE_AUTH_TOKEN)\s*=\s*(?!<|\$|\$\{|\[REDACTED\]|redacted\b)[^\s#'"]{4,}/giu,
      "[REDACTED_ENV]=[REDACTED]",
    )
    .replace(/\/(?:private\/)?tmp\/[^\s"']*/gu, "<temp-path>")
    .replace(/\/var\/folders\/[^\s"']*/gu, "<temp-path>")
    .replace(/\/Users\/[^/\s"']+(?:\/[^\s"']*)?/gu, "/" + "Users/[REDACTED]")
    .replace(/\/home\/(?:<user>|[^<\s/]+)(?:\/[^\s"']*)?/gu, "/" + "home/[REDACTED]")
    .replace(/[A-Z]:\\Users\\[^\\\s"']+(?:\\[^\s"']*)?/gu, "C:" + "\\Users\\[REDACTED]");
}

function addDiagnostic(diagnostics, code, message, details = {}) {
  diagnostics.push({
    code,
    message: redact(message),
    ...Object.fromEntries(
      Object.entries(details).map(([key, value]) => [key, typeof value === "string" ? redact(value) : value]),
    ),
  });
}

function validateEvidence(evidence, rawText = JSON.stringify(evidence), options = {}) {
  const diagnostics = [];

  if (evidence?.schemaVersion !== MATRIX_SCHEMA_VERSION && evidence?.schemaVersion !== LEGACY_EVIDENCE_SCHEMA_VERSION) {
    addDiagnostic(diagnostics, "invalid_schema", "Real compatibility evidence schema version is missing or unsupported.", {
      expected: MATRIX_SCHEMA_VERSION,
      actual: typeof evidence?.schemaVersion === "string" ? evidence.schemaVersion : null,
    });
  }

  checkUnsafeText(rawText, diagnostics);
  checkDirtyState(evidence, diagnostics);
  const targetSha = checkTargetSha(evidence, options, diagnostics);
  const freshness = checkFreshness(evidence, options, diagnostics);
  const dirtyPolicy = checkDirtyPolicy(evidence, options, diagnostics);
  checkCommands(evidence, diagnostics);
  if (evidence?.schemaVersion === MATRIX_SCHEMA_VERSION) {
    checkMatrixAdapters(evidence, diagnostics);
  } else {
    checkSafeSmokes(evidence, diagnostics);
    checkAuthenticatedSmokes(evidence, diagnostics);
    checkNeedsVerification(evidence, diagnostics);
  }
  checkPackageBoundary(evidence, diagnostics);

  return verificationResult(evidence, diagnostics, { targetSha, freshness, dirtyPolicy });
}

function verificationResult(evidence, diagnostics, context) {
  return {
    schemaVersion: VERIFICATION_SCHEMA_VERSION,
    ok: diagnostics.length === 0,
    evidenceSchemaVersion: evidence?.schemaVersion ?? null,
    file: evidence?.schemaVersion ? DEFAULT_FILE : null,
    checkedAt: new Date().toISOString(),
    targetSha: context.targetSha,
    freshness: context.freshness,
    dirtyPolicy: context.dirtyPolicy,
    diagnosticSummary: summarizeDiagnostics(diagnostics),
    diagnostics,
  };
}

function evidenceSha(evidence) {
  if (evidence?.schemaVersion === MATRIX_SCHEMA_VERSION) return typeof evidence?.gitSha === "string" ? evidence.gitSha : null;
  return typeof evidence?.gitHeadSha === "string" ? evidence.gitHeadSha : null;
}

function evidenceCheckedAt(evidence) {
  if (typeof evidence?.checkedAt === "string") return evidence.checkedAt;
  if (typeof evidence?.generatedAt === "string") return evidence.generatedAt;
  return null;
}

function checkTargetSha(evidence, options, diagnostics) {
  const expected = options.targetSha ?? null;
  const actual = evidenceSha(evidence);
  if (expected === null) {
    return { expected: null, actual, ok: null, status: "not_requested" };
  }
  if (!/^[0-9a-f]{40}$/u.test(expected)) {
    addDiagnostic(diagnostics, "target_sha_invalid", "Target SHA must be a full 40-character lowercase hex commit SHA.", {
      expected,
    });
    return { expected, actual, ok: false, status: "invalid_target_sha" };
  }
  if (typeof actual !== "string" || !/^[0-9a-f]{40}$/u.test(actual)) {
    addDiagnostic(diagnostics, "target_sha_mismatch", "Evidence does not include a valid git SHA for target comparison.", {
      expected,
      actual,
    });
    return { expected, actual, ok: false, status: "missing_evidence_sha" };
  }
  if (actual !== expected) {
    addDiagnostic(diagnostics, "target_sha_mismatch", "Evidence git SHA does not match the requested target SHA.", {
      expected,
      actual,
    });
    return { expected, actual, ok: false, status: "mismatched" };
  }
  return { expected, actual, ok: true, status: "matched" };
}

function checkFreshness(evidence, options, diagnostics) {
  const maxAgeHours = options.maxAgeHours ?? null;
  const checkedAt = evidenceCheckedAt(evidence);
  if (maxAgeHours === null) {
    return { evidenceCheckedAt: checkedAt, maxAgeHours: null, ageHours: null, ok: null, status: "not_requested" };
  }
  const checkedTime = checkedAt === null ? NaN : Date.parse(checkedAt);
  if (!Number.isFinite(checkedTime)) {
    addDiagnostic(diagnostics, "checked_at_invalid", "Evidence checkedAt/generatedAt is missing or invalid.", {
      checkedAt,
    });
    return { evidenceCheckedAt: checkedAt, maxAgeHours, ageHours: null, ok: false, status: "invalid_checked_at" };
  }
  const ageHours = roundHours((Date.now() - checkedTime) / 3_600_000);
  if (ageHours < 0) {
    addDiagnostic(diagnostics, "checked_at_invalid", "Evidence checkedAt/generatedAt is in the future.", {
      checkedAt,
    });
    return { evidenceCheckedAt: checkedAt, maxAgeHours, ageHours, ok: false, status: "invalid_checked_at" };
  }
  if (ageHours > maxAgeHours) {
    addDiagnostic(diagnostics, "evidence_too_old", "Evidence is older than the requested max-age-hours threshold.", {
      maxAgeHours,
      ageHours,
    });
    return { evidenceCheckedAt: checkedAt, maxAgeHours, ageHours, ok: false, status: "expired" };
  }
  return { evidenceCheckedAt: checkedAt, maxAgeHours, ageHours, ok: true, status: "fresh" };
}

function checkDirtyPolicy(evidence, options, diagnostics) {
  const dirty = dirtyPolicyState(evidence);
  const policy = options.releaseStrict ? "release-strict" : "observe";
  const allowDirty = options.allowDirty === true;
  if (dirty.inputDirty !== true) {
    return {
      policy,
      allowDirty,
      gitDirty: dirty.gitDirty,
      inputDirty: dirty.inputDirty,
      outputDirty: dirty.outputDirty,
      ok: true,
      status: dirty.outputDirty === true ? "self_dirty_only" : dirty.gitDirty === false ? "clean" : "unknown",
    };
  }
  if (options.releaseStrict && !allowDirty) {
    addDiagnostic(diagnostics, "dirty_evidence_not_allowed", "Dirty input evidence is not valid for release-strict review without --allow-dirty.", {
      policy,
    });
    return {
      policy,
      allowDirty,
      gitDirty: dirty.gitDirty,
      inputDirty: dirty.inputDirty,
      outputDirty: dirty.outputDirty,
      ok: false,
      status: "dirty_requires_allow",
    };
  }
  return {
    policy,
    allowDirty,
    gitDirty: dirty.gitDirty,
    inputDirty: dirty.inputDirty,
    outputDirty: dirty.outputDirty,
    ok: true,
    status: allowDirty ? "dirty_allowed" : "dirty_present",
  };
}

function dirtyPolicyState(evidence) {
  const gitDirty = typeof evidence?.gitDirty === "boolean" ? evidence.gitDirty : null;
  if (evidence?.schemaVersion !== MATRIX_SCHEMA_VERSION) {
    return { gitDirty, inputDirty: gitDirty, outputDirty: false };
  }
  const summary = evidence?.dirtySummary ?? {};
  const outputPath = typeof summary.outputPath === "string" ? summary.outputPath : ".release-evidence/p8-2-real-cli-compatibility-matrix.json";
  const inputDirty = typeof evidence?.gitInputDirty === "boolean"
    ? evidence.gitInputDirty
    : typeof summary.inputAfterWrite?.dirty === "boolean"
      ? summary.inputAfterWrite.dirty
      : typeof summary.afterWrite?.dirty === "boolean"
        ? dirtyStateExcludingPath(summary.afterWrite, outputPath).dirty
        : gitDirty;
  const outputDirty = typeof evidence?.gitOutputDirty === "boolean"
    ? evidence.gitOutputDirty
    : typeof summary.outputAfterWrite?.dirty === "boolean"
      ? summary.outputAfterWrite.dirty
      : typeof summary.afterWrite?.dirty === "boolean"
        ? dirtyStateOnlyPath(summary.afterWrite, outputPath).dirty
        : false;
  return { gitDirty, inputDirty, outputDirty };
}

function summarizeDiagnostics(diagnostics) {
  return {
    count: diagnostics.length,
    codes: [...new Set(diagnostics.map((diagnostic) => diagnostic.code).filter(Boolean))].sort(),
  };
}

function roundHours(value) {
  return Math.round(value * 100) / 100;
}

function dirtyStateExcludingPath(state, excludedPath) {
  const changedFiles = Array.isArray(state?.changedFiles)
    ? state.changedFiles.filter((entry) => !matchesGitPath(entry?.path, excludedPath))
    : [];
  return { dirty: changedFiles.length > 0, changedFiles };
}

function dirtyStateOnlyPath(state, includedPath) {
  const changedFiles = Array.isArray(state?.changedFiles)
    ? state.changedFiles.filter((entry) => matchesGitPath(entry?.path, includedPath))
    : [];
  return { dirty: changedFiles.length > 0, changedFiles };
}

function matchesGitPath(candidate, expectedPath) {
  if (typeof candidate !== "string" || typeof expectedPath !== "string" || expectedPath === "<external_evidence_file>") return false;
  if (candidate === expectedPath) return true;
  return candidate.split(" -> ").some((part) => part === expectedPath);
}

function checkUnsafeText(rawText, diagnostics) {
  for (const { code, name, pattern } of UNSAFE_PATTERNS) {
    if (pattern.test(rawText)) addDiagnostic(diagnostics, code, `Real compatibility evidence contains unredacted ${name}.`);
  }
}

function checkDirtyState(evidence, diagnostics) {
  const before = evidence?.gitStatusBeforeWrite;
  const after = evidence?.gitStatusAfterWrite;
  const isMatrix = evidence?.schemaVersion === MATRIX_SCHEMA_VERSION;
  const sha = isMatrix ? evidence?.gitSha : evidence?.gitHeadSha;
  const requiredFields = isMatrix
    ? ["gitSha", "gitDirty", "dirtySummary"]
    : ["gitHeadSha", "gitDirty", "gitStatusBeforeWrite", "gitStatusAfterWrite"];
  for (const field of requiredFields) {
    if (!(field in (evidence ?? {}))) {
      addDiagnostic(diagnostics, "missing_dirty_state", `Evidence is missing ${field}.`, { field });
    }
  }
  if (typeof sha !== "string" || !/^[0-9a-f]{40}$/u.test(sha)) {
    addDiagnostic(diagnostics, "missing_dirty_state", `${isMatrix ? "gitSha" : "gitHeadSha"} must be a full commit SHA.`);
  }
  if (typeof evidence?.gitDirty !== "boolean") {
    addDiagnostic(diagnostics, "missing_dirty_state", "gitDirty must be a boolean.");
  }
  if (isMatrix) {
    const dirtySummary = evidence?.dirtySummary;
    for (const [label, state] of [["dirtySummary.beforeWrite", dirtySummary?.beforeWrite], ["dirtySummary.afterWrite", dirtySummary?.afterWrite]]) {
      if (typeof state?.dirty !== "boolean" || typeof state?.changedFilesCount !== "number" || !Array.isArray(state?.changedFiles)) {
        addDiagnostic(diagnostics, "missing_dirty_state", `${label} must include dirty, changedFilesCount, and changedFiles.`, {
          field: label,
        });
      }
    }
    for (const [label, state] of [
      ["dirtySummary.inputBeforeWrite", dirtySummary?.inputBeforeWrite],
      ["dirtySummary.inputAfterWrite", dirtySummary?.inputAfterWrite],
      ["dirtySummary.outputBeforeWrite", dirtySummary?.outputBeforeWrite],
      ["dirtySummary.outputAfterWrite", dirtySummary?.outputAfterWrite],
    ]) {
      if (state !== undefined && (typeof state?.dirty !== "boolean" || typeof state?.changedFilesCount !== "number" || !Array.isArray(state?.changedFiles))) {
        addDiagnostic(diagnostics, "missing_dirty_state", `${label} must include dirty, changedFilesCount, and changedFiles.`, {
          field: label,
        });
      }
    }
    if (typeof evidence?.gitInputDirty === "boolean" && evidence?.gitDirty !== evidence.gitInputDirty) {
      addDiagnostic(diagnostics, "missing_dirty_state", "gitDirty must match gitInputDirty for matrix evidence.");
    } else if (typeof evidence?.gitInputDirty !== "boolean" && typeof dirtySummary?.afterWrite?.dirty === "boolean" && evidence?.gitDirty !== dirtySummary.afterWrite.dirty) {
      addDiagnostic(diagnostics, "missing_dirty_state", "legacy matrix gitDirty must match dirtySummary.afterWrite.dirty.");
    }
    if (typeof evidence?.gitInputDirty === "boolean" && typeof dirtySummary?.inputAfterWrite?.dirty === "boolean" && evidence.gitInputDirty !== dirtySummary.inputAfterWrite.dirty) {
      addDiagnostic(diagnostics, "missing_dirty_state", "gitInputDirty must match dirtySummary.inputAfterWrite.dirty.");
    }
    if (typeof evidence?.gitOutputDirty === "boolean" && typeof dirtySummary?.outputAfterWrite?.dirty === "boolean" && evidence.gitOutputDirty !== dirtySummary.outputAfterWrite.dirty) {
      addDiagnostic(diagnostics, "missing_dirty_state", "gitOutputDirty must match dirtySummary.outputAfterWrite.dirty.");
    }
    return;
  }
  for (const [label, state] of [["gitStatusBeforeWrite", before], ["gitStatusAfterWrite", after]]) {
    if (typeof state?.dirty !== "boolean" || typeof state?.changedFilesCount !== "number" || !Array.isArray(state?.changedFiles)) {
      addDiagnostic(diagnostics, "missing_dirty_state", `${label} must include dirty, changedFilesCount, and changedFiles.`, {
        field: label,
      });
    }
    if (state?.headSha && state.headSha !== evidence?.gitHeadSha) {
      addDiagnostic(diagnostics, "missing_dirty_state", `${label} headSha does not match gitHeadSha.`, { field: label });
    }
  }
  if (typeof after?.dirty === "boolean" && evidence?.gitDirty !== after.dirty) {
    addDiagnostic(diagnostics, "missing_dirty_state", "gitDirty must match gitStatusAfterWrite.dirty.");
  }
  if (evidence?.gitDirty === true && /clean\s+commit\s+evidence|cleanCommitEvidence/iu.test(JSON.stringify(evidence))) {
    addDiagnostic(diagnostics, "missing_dirty_state", "Dirty-tree evidence must not be described as clean commit evidence.");
  }
}

function checkMatrixAdapters(evidence, diagnostics) {
  const adapters = evidence?.adapters;
  if (!adapters || typeof adapters !== "object" || Array.isArray(adapters)) {
    addDiagnostic(diagnostics, "invalid_schema", "adapters must be an object keyed by codex, claude, and opencode.");
    return;
  }
  for (const adapter of ADAPTERS) {
    const entry = adapters[adapter];
    if (!entry || typeof entry !== "object") {
      addDiagnostic(diagnostics, "adapter_missing", `P8-2 matrix is missing adapter ${adapter}.`, { adapter });
      continue;
    }
    checkRequiredAdapterFields(adapter, entry, diagnostics);
    checkExecutable(adapter, entry.executable, diagnostics);
    checkAdapterSkipStates(adapter, entry, diagnostics);
    checkMatrixNeedsVerification(adapter, entry, diagnostics);
    if (containsUnsafeCommandSummaryField(entry) || containsUnsafeAuthenticatedField(entry)) {
      addDiagnostic(diagnostics, "unsafe_content", `Adapter ${adapter} contains raw output, prompt text, or final run records.`, { adapter });
    }
  }
}

function checkRequiredAdapterFields(adapter, entry, diagnostics) {
  for (const field of [
    "executable",
    "version",
    "auth",
    "modelsSource",
    "capabilities",
    "argvProfile",
    "parserMode",
    "promptTransport",
    "safePreflight",
    "optionalSmoke",
    "diagnostics",
    "needsVerification",
  ]) {
    if (!(field in entry)) addDiagnostic(diagnostics, "adapter_field_missing", `Adapter ${adapter} is missing ${field}.`, { adapter, field });
  }
  if (!Array.isArray(entry.diagnostics)) {
    addDiagnostic(diagnostics, "adapter_field_missing", `Adapter ${adapter} diagnostics must be an array.`, { adapter, field: "diagnostics" });
  }
  if (!Array.isArray(entry.needsVerification)) {
    addDiagnostic(diagnostics, "needs_verification_missing", `Adapter ${adapter} needsVerification must be an array.`, { adapter });
  }
}

function checkExecutable(adapter, executable, diagnostics) {
  if (!executable || typeof executable !== "object") {
    addDiagnostic(diagnostics, "adapter_field_missing", `Adapter ${adapter} executable resolution is missing.`, { adapter, field: "executable" });
    return;
  }
  if (executable.status !== "resolved" && executable.status !== "unavailable") {
    addDiagnostic(diagnostics, "invalid_schema", `Adapter ${adapter} executable.status must be resolved or unavailable.`, { adapter });
  }
  if (executable.status === "resolved" && executable.path !== "<resolved_executable>") {
    addDiagnostic(diagnostics, "unsafe_content", `Adapter ${adapter} resolved executable path must be redacted.`, { adapter });
  }
  if (executable.status === "unavailable" && typeof executable.unavailableReason !== "string") {
    addDiagnostic(diagnostics, "adapter_detection_missing", `Adapter ${adapter} must include an unavailable reason when executable is unavailable.`, {
      adapter,
    });
  }
}

function checkAdapterSkipStates(adapter, entry, diagnostics) {
  const safe = entry.safePreflight;
  const optional = entry.optionalSmoke;
  if (!safe || typeof safe !== "object") {
    addDiagnostic(diagnostics, "adapter_field_missing", `Adapter ${adapter} safePreflight is missing.`, { adapter });
  } else if (safe.runClassification !== "success" && safe.ok === true) {
    addDiagnostic(diagnostics, "skip_state_claimed_as_success", `Adapter ${adapter} safe preflight skip/block must not be marked success.`, {
      adapter,
    });
  }
  if (!optional || typeof optional !== "object") {
    addDiagnostic(diagnostics, "adapter_field_missing", `Adapter ${adapter} optionalSmoke is missing.`, { adapter });
    return;
  }
  const status = optional.status ?? optional.runClassification;
  if (status !== "success" && optional.ok === true) {
    addDiagnostic(diagnostics, "skip_state_claimed_as_success", `Adapter ${adapter} optional smoke skip/block must not be marked success.`, {
      adapter,
    });
  }
  if (status === "success") {
    for (const [field, expected] of Object.entries(AUTHENTICATED_SUCCESS_REQUIRED)) {
      if (optional[field] !== expected) {
        addDiagnostic(diagnostics, "authenticated_success_incomplete", `Adapter ${adapter} optional smoke success is missing required ${field} evidence.`, {
          adapter,
          field,
        });
      }
    }
    if (typeof optional.expectedTextSha256 !== "string" || !/^[0-9a-f]{64}$/u.test(optional.expectedTextSha256)) {
      addDiagnostic(diagnostics, "authenticated_success_incomplete", `Adapter ${adapter} optional smoke success must include expectedTextSha256.`, {
        adapter,
      });
    }
  }
}

function checkMatrixNeedsVerification(adapter, entry, diagnostics) {
  const mapsTo = new Set((entry.needsVerification ?? []).map((candidate) => candidate?.mapsTo).filter(Boolean));
  for (const required of REQUIRED_NEEDS_VERIFICATION[adapter] ?? []) {
    if (!mapsTo.has(required)) {
      addDiagnostic(diagnostics, "needs_verification_missing", `Required needsVerification item is missing for ${adapter}.`, {
        adapter,
        item: required,
      });
    }
  }
  const knownNeedsVerification = (entry.argvProfile?.knownFlags ?? []).filter((flag) => flag?.status === "needs_verification");
  for (const flag of knownNeedsVerification) {
    if (!mapsTo.has(flag.mapsTo)) {
      addDiagnostic(diagnostics, "needs_verification_missing", `Known flag marked needs_verification is missing from adapter ${adapter} needsVerification.`, {
        adapter,
        item: flag.mapsTo,
      });
    }
  }
}

function checkCommands(evidence, diagnostics) {
  const commands = evidence?.commands;
  if (!Array.isArray(commands) || commands.length === 0) {
    addDiagnostic(diagnostics, "invalid_schema", "commands must be a non-empty array.");
    return;
  }
  commands.forEach((command, index) => {
    const extraKeys = Object.keys(command ?? {}).filter((key) => !COMMAND_ALLOWED_KEYS.has(key));
    if (extraKeys.length > 0) {
      addDiagnostic(diagnostics, "unsafe_content", "Command summary contains fields outside the redacted allowlist.", { index });
    }
    if (typeof command?.command !== "string" || typeof command?.exitCode !== "number") {
      addDiagnostic(diagnostics, "invalid_schema", "Command summary must include command and exitCode.", { index });
    }
    if ("stdout" in (command ?? {}) || "stderr" in (command ?? {}) || "rawStdout" in (command ?? {}) || "rawStderr" in (command ?? {})) {
      addDiagnostic(diagnostics, "unsafe_content", "Command summary must not include raw stdout or stderr.", { index });
    }
    if (containsUnsafeCommandSummaryField(command?.summary)) {
      addDiagnostic(diagnostics, "unsafe_content", "Command summary must not include raw output, prompt text, or final run records.", {
        index,
      });
    }
  });
}

function containsUnsafeCommandSummaryField(value) {
  const stack = [value];
  while (stack.length > 0) {
    const current = stack.pop();
    if (!current || typeof current !== "object") continue;
    for (const [key, child] of Object.entries(current)) {
      if (COMMAND_SUMMARY_UNSAFE_KEYS.has(key)) return true;
      if (key === "prompt" && typeof child === "string") return true;
      if (child && typeof child === "object") stack.push(child);
    }
  }
  return false;
}

function checkSafeSmokes(evidence, diagnostics) {
  const safeSmokes = evidence?.safeRealSmokes;
  if (!safeSmokes || typeof safeSmokes !== "object") {
    addDiagnostic(diagnostics, "invalid_schema", "safeRealSmokes must be present.");
    return;
  }
  const expectedDefaults = {
    codex: "real_run_skipped",
    claude: "auth_missing",
    opencode: "real_run_skipped",
  };
  for (const [agent, expected] of Object.entries(expectedDefaults)) {
    const smoke = safeSmokes[agent];
    if (!smoke) {
      addDiagnostic(diagnostics, "invalid_schema", `safeRealSmokes is missing ${agent}.`, { agent });
      continue;
    }
    if (smoke.runClassification === "success" || smoke.ok === true) {
      addDiagnostic(diagnostics, "skip_state_claimed_as_success", `Safe real smoke for ${agent} must not be counted as success.`, {
        agent,
      });
    }
    if (smoke.runClassification !== expected) {
      addDiagnostic(diagnostics, "skip_state_claimed_as_success", `Safe real smoke for ${agent} has unexpected default classification.`, {
        agent,
        expected,
        actual: smoke.runClassification ?? null,
      });
    }
  }
}

function checkAuthenticatedSmokes(evidence, diagnostics) {
  const smokes = evidence?.authenticatedRealSmokes;
  if (!Array.isArray(smokes)) {
    addDiagnostic(diagnostics, "invalid_schema", "authenticatedRealSmokes must be an array.");
    return;
  }
  for (const [index, smoke] of smokes.entries()) {
    if (smoke?.ok === true && smoke.runClassification !== "success") {
      addDiagnostic(diagnostics, "skip_state_claimed_as_success", "Authenticated smoke ok=true must only be used with success.", { index });
    }
    if (containsUnsafeAuthenticatedField(smoke)) {
      addDiagnostic(diagnostics, "unsafe_content", "Authenticated smoke contains raw output, prompt text, token, or private path fields.", {
        index,
      });
    }
    if (smoke?.runClassification !== "success") continue;
    for (const [field, expected] of Object.entries(AUTHENTICATED_SUCCESS_REQUIRED)) {
      if (smoke[field] !== expected) {
        addDiagnostic(diagnostics, "authenticated_success_incomplete", `Authenticated success is missing required ${field} evidence.`, {
          index,
          field,
        });
      }
    }
  }
}

function containsUnsafeAuthenticatedField(value) {
  const stack = [value];
  while (stack.length > 0) {
    const current = stack.pop();
    if (!current || typeof current !== "object") continue;
    for (const [key, child] of Object.entries(current)) {
      if (AUTHENTICATED_SUCCESS_UNSAFE_KEYS.has(key)) return true;
      if (key === "prompt" && typeof child === "string") return true;
      if (child && typeof child === "object") stack.push(child);
    }
  }
  return false;
}

function checkNeedsVerification(evidence, diagnostics) {
  const audit = Array.isArray(evidence?.needsVerificationAudit) ? evidence.needsVerificationAudit : [];
  if (audit.length === 0) {
    addDiagnostic(diagnostics, "needs_verification_missing", "needsVerificationAudit must be present.");
    return;
  }
  for (const [adapter, requiredItems] of Object.entries(REQUIRED_NEEDS_VERIFICATION)) {
    const item = audit.find((candidate) => candidate?.adapter === adapter);
    const mapsTo = new Set((item?.items ?? []).map((candidate) => candidate?.mapsTo).filter(Boolean));
    for (const required of requiredItems) {
      if (!mapsTo.has(required)) {
        addDiagnostic(diagnostics, "needs_verification_missing", `Required needsVerification item is missing for ${adapter}.`, {
          adapter,
          item: required,
        });
      }
    }
  }
}

function checkPackageBoundary(evidence, diagnostics) {
  if (evidence?.packageBoundary?.releaseEvidenceIsRepoOnly !== true) {
    addDiagnostic(diagnostics, "package_boundary_invalid", "packageBoundary.releaseEvidenceIsRepoOnly must be true.");
  }
}

